// Derived values. Kept as properties of window so that
// any page can show a live article count and top picks
// without re-implementing the logic.
// -----------------------------------------------------
window.TSI_ARTICLE_COUNT = window.TSI_ARTICLES.length;

// Top Picks = articles with `featured: true`, fall back to newest 6 if fewer than 4.
(function () {
  var feat = window.TSI_ARTICLES.filter(function (a) { return a.featured; });
  if (feat.length < 4) {
    // take newest until we have at least 6
    var seen = new Set(feat.map(function (a) { return a.slug; }));
    for (var i = 0; i < window.TSI_ARTICLES.length && feat.length < 6; i++) {
      var a = window.TSI_ARTICLES[i];
      if (!seen.has(a.slug)) { feat.push(a); seen.add(a.slug); }
    }
  }
  window.TSI_TOP_PICKS = feat;
})();

// -----------------------------------------------------
// Renderers. Run automatically on DOMContentLoaded if the
// relevant container exists. Every container is optional
// so individual pages can use whichever they need.
// -----------------------------------------------------
(function () {
  function renderArticleGrid() {
    var grid = document.getElementById('article-grid');
    if (!grid || !window.TSI_ARTICLES) return;
    if (!window.TSI_CARDS) {
      if (window.console) console.warn('TSI_CARDS missing — ensure cards.js loads before articles.js');
      return;
    }
    // Respect an optional data-limit attribute (e.g. data-limit="9" on homepage).
    // When absent, show everything.
    var limit = parseInt(grid.getAttribute('data-limit'), 10);
    var list = window.TSI_ARTICLES;
    if (!isNaN(limit) && limit > 0) list = list.slice(0, limit);
    grid.innerHTML = list.map(function (a) {
      return window.TSI_CARDS.render(a, 'grid');
    }).join('');
  }

  function renderHeroCount() {
    var el = document.getElementById('article-count');
    if (el) el.textContent = window.TSI_ARTICLE_COUNT;
  }

  function renderTicker() {
    var el = document.getElementById('topic-ticker');
    if (!el || !window.TSI_ARTICLES) return;
    // Take the first 12 article categories deduplicated with the title
    var topics = window.TSI_ARTICLES.slice(0, 12).map(function (a) {
      // short topic phrase derived from title
      return a.title
        .replace(/^How to (?:Make Money |Become an? |Start a |Get Paid to )?/i, '')
        .replace(/ in the UK.*/i, '')
        .replace(/ UK.*/i, '')
        .replace(/ for \d{4}.*/i, '')
        .replace(/ \(.*$/, '')
        .replace(/ — .*$/, '')
        .trim();
    });
    // Repeat twice for seamless marquee
    var all = topics.concat(topics);
    el.innerHTML = all.map(function (t, i) {
      var sep = i < all.length - 1 ? '<span>•</span>' : '';
      return '<span>' + t + '</span>' + sep;
    }).join('');
  }

  // Category pages: body has class "category-page" and data-category-filter="<label>".
  // Populates #cat-featured-grid (featured:true entries) and #cat-catalogue-grid
  // (all entries) with TSI_CARDS.render(a, 'grid'). Empty state message when
  // no articles match the filter yet.
  function renderCategoryPage() {
    var body = document.body;
    if (!body || !body.classList.contains('category-page')) return;
    var filter = body.getAttribute('data-category-filter');
    if (!filter || !window.TSI_ARTICLES) return;
    if (!window.TSI_CARDS) {
      if (window.console) console.warn('TSI_CARDS missing — ensure cards.js loads before articles.js');
      return;
    }

    var inCat = window.TSI_ARTICLES.filter(function (a) { return a.category === filter; });
    var featured = inCat.filter(function (a) { return a.featured; });

    var catalogueGrid = document.getElementById('cat-catalogue-grid');
    var catalogueEmpty = document.getElementById('cat-empty-msg');
    if (catalogueGrid) {
      catalogueGrid.innerHTML = inCat.map(function (a) {
        return window.TSI_CARDS.render(a, 'grid');
      }).join('');
    }
    if (catalogueEmpty) {
      if (inCat.length === 0) catalogueEmpty.removeAttribute('hidden');
      else catalogueEmpty.setAttribute('hidden', '');
    }

    var featuredSection = document.getElementById('cat-featured-section');
    var featuredGrid = document.getElementById('cat-featured-grid');
    if (featuredGrid && featured.length > 0) {
      featuredGrid.innerHTML = featured.map(function (a) {
        return window.TSI_CARDS.render(a, 'grid');
      }).join('');
      if (featuredSection) featuredSection.removeAttribute('hidden');
    } else if (featuredSection) {
      featuredSection.setAttribute('hidden', '');
    }
  }

  function init() {
    renderHeroCount();
    renderArticleGrid();
    renderTicker();
    renderCategoryPage();
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
})();
